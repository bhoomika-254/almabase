"""
Multi-format export service.
Generates PDF, DOCX, HTML, JSON, and CSV from answer data.
"""
import csv
import io
import json


def _display_answer(answer: dict) -> str:
    if answer.get("user_edited") and answer.get("edited_answer"):
        return answer["edited_answer"]
    return answer.get("generated_answer") or "Not present in the document"


def _format_citations(citations: list) -> str:
    if not citations:
        return ""
    return " | ".join(
        f'[{i+1}] {c.get("doc_name", "")} — "{c.get("excerpt", "")}"'
        for i, c in enumerate(citations)
    )


# ─── JSON Export ─────────────────────────────────────────────────────────────

def export_json(answers: list[dict], coverage: dict | None, project_name: str) -> bytes:
    payload = {
        "project": project_name,
        "coverage": coverage,
        "answers": [
            {
                "question_number": a["question_number"],
                "question_text": a["question_text"],
                "answer": _display_answer(a),
                "confidence_score": a.get("confidence_score"),
                "not_found": a.get("not_found", False),
                "user_edited": a.get("user_edited", False),
                "citations": a.get("citations") or [],
                "evidence_snippets": a.get("evidence_snippets") or [],
                "all_candidates": a.get("answer_candidates") or [],
            }
            for a in answers
        ],
    }
    return json.dumps(payload, indent=2, default=str).encode("utf-8")


# ─── CSV Export ──────────────────────────────────────────────────────────────

def export_csv(answers: list[dict]) -> bytes:
    output = io.StringIO()
    writer = csv.writer(output)
    writer.writerow([
        "Question #", "Question Text", "Answer",
        "Confidence Score", "Not Found", "User Edited", "Citations",
    ])
    for a in answers:
        writer.writerow([
            a["question_number"],
            a["question_text"],
            _display_answer(a),
            f"{round((a.get('confidence_score') or 0) * 100)}%",
            "Yes" if a.get("not_found") else "No",
            "Yes" if a.get("user_edited") else "No",
            _format_citations(a.get("citations") or []),
        ])
    return output.getvalue().encode("utf-8-sig")  # BOM for Excel


# ─── HTML Export ─────────────────────────────────────────────────────────────

